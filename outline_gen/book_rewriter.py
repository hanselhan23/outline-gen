@dataclass
class OutlineEntry:
    """Represents a single outline node parsed from outline TXT."""

    title: str
    page: int  # 1-indexed global page number
    level: int  # 0 = top-level, increasing with depth
    children: List["OutlineEntry"] = field(default_factory=list)
    parent: Optional["OutlineEntry"] = None


