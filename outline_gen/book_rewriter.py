"""Tools for rewriting a book into concise Chinese text using LLM.

Workflow:
- Parse an outline TXT (generated by outline-gen) into a tree.
- Treat leaf nodes as the smallest semantic units.
- For each leaf node, extract the corresponding page range from the PDF.
- Ask the LLM to produce a concise Chinese rewrite for that leaf,
  organized according to a recommended logical skeleton for the book type.
- Save each rewritten leaf as a Markdown file under a MkDocs project
  directory, using the mkdocs-material theme.
"""

